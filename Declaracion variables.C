//Declarar variables en funciones

//Para declarar una variable primero debemos colocar (opcionalmente) el modificador, luego el tipo de variable y seguido el nombre.

unsigned int var;

//Al declarar una variable podemos darle un valor inicial igualando la variable al valor

unsigned int var=123;

//Si quiero declarar mas de una variable podemos hacerlo una debajo de la otra o separando por comas, siempre y cuando sean variables del mismo tipo.

unsigned int var1;
unsigned int var2;
unsigned int var3;
//seria lo mismo que...
unsigned int var1, var2, var3;

